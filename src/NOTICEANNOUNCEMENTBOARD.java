    public void tableDetails() {
        DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();
        dtm.setRowCount(0); // clear table
        try {
            Connection con = ConnectionProvider.getCon();
            Statement st = con.createStatement();
            ResultSet rs = st.executeQuery("SELECT * FROM notice_board");
            while (rs.next()) {
                dtm.addRow(new Object[]{
                        rs.getInt("notice_id"),
                        rs.getString("title"),
                        rs.getString("description"),
                        rs.getDate("post_date")
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

    // Auto-generate notice ID
    public void autoSetNoticeID() {
        try (Connection con = ConnectionProvider.getCon();
             Statement st = con.createStatement();
             ResultSet rs = st.executeQuery("SELECT MAX(notice_id) FROM notice_board")) {

            int id = 1;
            if (rs.next() && rs.getInt(1) != 0) {
                id = rs.getInt(1) + 1;
            }
            jTextField4.setText(String.valueOf(id));

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

