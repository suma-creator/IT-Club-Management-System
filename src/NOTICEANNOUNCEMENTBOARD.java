    private void jButton7ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton7ActionPerformed
        // TODO add your handling code here:
          try {
        Connection con = ConnectionProvider.getCon();
        PreparedStatement ps = con.prepareStatement(
                "SELECT * FROM notice_board WHERE notice_id=?"
        );
        ps.setInt(1, Integer.parseInt(jTextField4.getText()));
        ResultSet rs = ps.executeQuery();

        if (rs.next()) {
            // Fill all fields including Notice ID
            jTextField4.setText(String.valueOf(rs.getInt("notice_id")));
            jTextField7.setText(rs.getString("title"));
            jTextField5.setText(rs.getString("description"));
            jTextField6.setText(rs.getString("post_date").toString()); // convert date to string
        } else {
            JOptionPane.showMessageDialog(null, "Notice not found!");
            resetForm(); // clear fields only
        }

    } catch (NumberFormatException nfe) {
        JOptionPane.showMessageDialog(null, "Enter a valid Notice ID!");
    } catch (Exception e) {
        JOptionPane.showMessageDialog(null, e);
    }
    }//GEN-LAST:event_jButton7ActionPerformed

